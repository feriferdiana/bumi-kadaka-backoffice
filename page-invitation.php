            $("#btnGabung").click(function() {
                $("#invitation").hide();
                $("#joined").show("slow");
            });

            $("#btnNew").click(function() {
                $("#errorMsg").remove();
                $("#boxes").hide("slow");
                $("#msgConfirmation").hide("slow");
                $("#msgJoined").show("slow");
            });

            $("#btnExisting").click(function() {
                $("#errorMsg").remove();
                $("#boxes").hide("slow");
                $("#msgConfirmation").hide("slow");
                $("#msgHello").show("slow");
            });
            
            //
            var clicked = 0;
            $(".toggle-password").click(function (e) {
                e.preventDefault();

                $(this).toggleClass("toggle-password");
                if (clicked == 0) {
                $(this).html('<span class="material-icons md-18">visibility_off</span >');
                    clicked = 1;
                } else {
                    $(this).html('<span class="material-icons md-18">visibility</span >');
                    clicked = 0;
                }

                var input = $($(this).attr("toggle"));
                console.log(input);
                if (input.attr("type") == "password") {
                    input.attr("type", "text");
                } else {
                    input.attr("type", "password");
                }
            });

            $("#checkboXID").click(function() {
                var checked_status = this.checked;
                if (checked_status == true) {
                    $("#btnGabung").removeAttr("disabled");
                } else {
                    $("#btnGabung").attr("disabled", "disabled");
                }
            });

	    });
